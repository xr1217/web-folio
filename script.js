function toggleAbout() {
	var ele = document.getElementById("toggleAbout");
	var text = document.getElementById("displayAbout");
	if(ele.style.display == "block") {
    		ele.style.display = "none";
  	}
	else {
		ele.style.display = "block";
	}
} 

